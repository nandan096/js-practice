let mytime=new Date() //object
console.log(mytime);                        //2024-08-14T11:12:02.290Z
console.log(mytime.toDateString())          //Wed Aug 14 2024
console.log(mytime.toISOString());          //2024-08-14T11:12:02.290Z
console.log(mytime.toJSON());               //2024-08-14T11:12:02.290Z
console.log(mytime.toLocaleDateString());   //8/14/2024
console.log(mytime.toString());             //Wed Aug 14 2024 11:12:02 GMT+0000 (Coordinated Universal Time)

